$(document).ready(function() {
    $('.particles-js-canvas-el').css('position', 'absolute');
    $(".button-collapse").sideNav();

    $(window).scroll(function() {
        parallax();
    });
});

function parallax() {
    var Yvalue = $(window).scrollTop();
    $('.logo').css('top', (-(Yvalue * 0.5)) + 'px');
    $('#particles-js').css('top', (-(Yvalue * 1.4)) + 'px');
}
